    private void guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarActionPerformed
        // TODO add your handling code here:
        String correo = Correo.getText();
        String tel = Tel.getText();
        String dir = Direccion.getText();
        String municipio = Municipio.getText();
        String user = usuario.getText();
        String name = nombre.getText();
        String contra = String.valueOf(pass.getPassword());
        String contrac = String.valueOf(cpass.getPassword());

        if(user.length() == 0 || name.length() == 0 || contra.length() == 0 || contrac.length() == 0){
            usuario.setText("");
            nombre.setText("");
            pass.setText("");
            cpass.setText("");
            Correo.setText("");
            Tel.setText("");
            Direccion.setText("");
            Municipio.setText("");
            JOptionPane.showMessageDialog(this, "Llene todos los campos para continuar.");
        }else{
            if(false){
                usuario.setText("");
                nombre.setText("");
                pass.setText("");
                cpass.setText("");
                Correo.setText("");
                Tel.setText("");
                Direccion.setText("");
                Municipio.setText("");
                JOptionPane.showMessageDialog(this, "Usuario inexistente.", "Mensaje", JOptionPane.ERROR_MESSAGE);
            }else{
                if(data.isEmpty()){
                    usuario.setText("");
                    nombre.setText("");
                    pass.setText("");
                    cpass.setText("");
                    Correo.setText("");
                    Tel.setText("");
                    Direccion.setText("");
                    Municipio.setText("");
                    JOptionPane.showMessageDialog(this, "Usuario inexistente.", "Mensaje", JOptionPane.ERROR_MESSAGE);
                }else{
                    NodoListaSimple bus = data.head;
                    while(bus != null){
                        Cliente temp = (Cliente) bus.content;
                        if(user.equals(temp.getUser())){
                           break;
                        }
                        bus = bus.next;
                    }
                    if(bus == null){
                        usuario.setText("");
                        nombre.setText("");
                        pass.setText("");
                        cpass.setText("");
                        Correo.setText("");
                        Tel.setText("");
                        Direccion.setText("");
                        Municipio.setText("");  
                        JOptionPane.showMessageDialog(this, "El usuario no existe.", "Mensaje", JOptionPane.ERROR_MESSAGE);
                    }else{
                        if(contra.equals(contrac)){
                            Cliente temp = (Cliente) bus.content;
                            String c = BCrypt.withDefaults().hashToString(8, contra.toCharArray());
                            temp.setPass(c);
                            temp.setName(name);
                            temp.setCorreo(correo);
                            temp.setTelefono(tel);
                            temp.setDir(dir);
                            temp.setMunicipio(municipio);
                            usuario.setText("");
                            nombre.setText("");
                            pass.setText("");
                            cpass.setText("");
                            Correo.setText("");
                            Tel.setText("");
                            Direccion.setText("");
                            Municipio.setText("");
                            JOptionPane.showMessageDialog(this, "Campos actualizados correctamente.");
                        }else{
                            usuario.setText("");
                            nombre.setText("");
                            pass.setText("");
                            cpass.setText("");
                            Correo.setText("");
                            Tel.setText("");
                            Direccion.setText("");
                            Municipio.setText("");
                            JOptionPane.showMessageDialog(this, "Las contrase침as no coinciden.", "Mensaje", JOptionPane.ERROR_MESSAGE);
                            
                        }
                    }
                }
            }

        }
    }//GEN-LAST:event_guardarActionPerformed

